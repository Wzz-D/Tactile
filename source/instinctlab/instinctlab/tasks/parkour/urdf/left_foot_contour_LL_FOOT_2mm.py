# Left foot sole contour (LL_FOOT frame), ~2mm arc-length resampled
# Units: meters. Closed polyline: last point equals first.
import matplotlib.pyplot as plt

def plot_points(points):
    """
    绘制一系列点，并保持 x 轴与 y 轴等比例
    :param points: 包含 (x, y) 坐标的列表，例如 [(1,2), (3,4), (5,6)]
    """
    if not points:
        print("点列表为空，无法绘图")
        return

    # 分离 x 和 y 坐标
    x_coords = [p[0] for p in points]
    y_coords = [p[1] for p in points]

    # 绘制散点图
    plt.scatter(x_coords, y_coords, color='blue', marker='o')

    # 设置坐标轴等比例
    plt.axis('equal')

    # 添加标签和标题
    plt.xlabel('X')
    plt.ylabel('Y')
    plt.title('Plot of Given Points (Equal Aspect Ratio)')
    plt.grid(True)

    # 显示图形
    plt.show()

