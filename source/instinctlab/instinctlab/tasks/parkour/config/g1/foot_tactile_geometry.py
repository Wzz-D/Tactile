def _translate_xy(
    points_xy: tuple[tuple[float, float], ...],
    offset_xy: tuple[float, float],
) -> list[list[float]]:
    """Translate local sole-plane points by a constant XY offset."""

    dx, dy = offset_xy
    return [[x + dx, y + dy] for x, y in points_xy]


